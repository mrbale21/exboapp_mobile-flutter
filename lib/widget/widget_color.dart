import 'package:flutter/material.dart';

class WidgetColor {
  static headColor() {
    return const Color.fromRGBO(35, 103, 109, 100);
  }

  static semiHeadColor() {
    return const Color.fromRGBO(47, 143, 157, 100);
  }

  static semiColor() {
    return const Color.fromRGBO(59, 172, 182, 100);
  }

  static semiLightColor() {
    return const Color.fromRGBO(130, 219, 216, 100);
  }

  static lightColor() {
    return const Color.fromRGBO(179, 232, 229, 100);
  }
}
